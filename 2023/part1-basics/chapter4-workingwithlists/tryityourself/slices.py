# slices
list = list(range(1, 10))

# slice first three items in list
print("The first three items in the list are: ")
print(list[0:3])
# slice middle of list
print("\nThe middle of the list is:")
print(list[3:6])
# slice last three of list
print("\nThe last three items of the list are:")
print(list[6:10])